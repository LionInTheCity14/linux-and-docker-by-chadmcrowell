pwd                     # present working directories.

mkdir -p dir1           # override the dir1 if it is already exists.

cp -r dir1 dir2         # copy the dir1 to dir2(if dir2 is not exists then it will make the dir2 then copy).

rmdir                   # to delete empty directories.

rm -r                   # to delete all types of directories(-r stands for recurrsively as it is directory and can contain contents).